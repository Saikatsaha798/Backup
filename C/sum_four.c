#include <stdio.h>

int main()
{
    float a, b, c, d;
    printf("Enter the first variable :\n");
    scanf("%f", &a);
    printf("Enter the second variable :\n");
    scanf("%f", &b);
    printf("Enter the third variable :\n");
    scanf("%f", &c);
    printf("Enter the fourth variable :\n");
    scanf("%f", &d);
    printf("The Sum of Four Numbers : %f", a + b + c + d);
    return 0;
}